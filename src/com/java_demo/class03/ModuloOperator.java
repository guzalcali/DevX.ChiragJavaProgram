package com.java_demo.class03;

public class ModuloOperator {
    public static void main(String[] args) {
        System.out.println(15 % 4); //3
        System.out.println(20 % 3); //2
        System.out.println(12 % 5); //2
        System.out.println(50 % 5); //0
        System.out.println(51 % 5); //1

        System.out.println(7 % 10); //7
        System.out.println(12 % 2); //0
        System.out.println(51 % 2); //1
        System.out.println(14 % 2); //0
        System.out.println(17 % 2); //1









    }
}
