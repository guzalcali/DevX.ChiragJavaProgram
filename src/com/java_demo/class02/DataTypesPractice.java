package com.java_demo.class02;

public class DataTypesPractice {
    // Create 6 types of variables byte, int, short, long, float, double & print into output

    public static void main(String args[]){
        byte num1 = 126;
        short num2 = -32767;
        int num3 = 5;
        long num4 = 922337209;
        float num6 = 5.34567889f;
        double num5 = 4.1723423534645645675676867867;
        // String we've to surround with double quotes
        String num7 = "This is my Java program!!";

        // Characters we've to surround with double quotes
        char num8 = 'a';

        // We can store only true and false into boolean variable
        boolean num9 = false;

        System.out.println("byte is " + num1);
        System.out.println("short is " + num2);
        System.out.println("int is " + num3);
        System.out.println("long is " + num4);
        System.out.println("double is " + num5);
        System.out.println("float is " +  num6);
        System.out.println("String is " +  num7);
        System.out.println("char is " +  num8);
        System.out.println("boolean is " +  num9);
    }

}
