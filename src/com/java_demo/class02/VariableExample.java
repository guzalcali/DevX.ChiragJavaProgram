package com.java_demo.class02;

public class VariableExample {

    public static void main(String args[]){
        // Declaration of variable
        int num1;

        // Assignment of value into variable
        num1 = 50;

        // Declaration and Initialization of variable
        int num2 = 10;

        // Declaration & Initialization of multiple variables into a single line
        int num3, num4=12, num5 = 90;

        String str = "Hello World";
        String num6 = "30" , num7 = "40";

        System.out.println(num2);
        System.out.println(num4);
        System.out.println(num5);
        System.out.println(str);
        System.out.println(num6 + num7);

    }
}
